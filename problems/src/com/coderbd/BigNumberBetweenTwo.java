package com.coderbd;

public class BigNumberBetweenTwo {

    public static void main(String[] args) {
        int x = 10;
        int y = -1000000000;//-10

        if (x > y) {
            System.out.println("x is greater than y");
        } else if (x < y) {
            System.out.println("X is less than Y");
        } else {
            System.out.println("X is equal To Y");
        }
    }

}
